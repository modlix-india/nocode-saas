CREATE TABLE IF NOT EXISTS entity_processor_product_ticket_ex_rules (
    ID BIGINT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT 'Primary key',
    APP_CODE CHAR(64) NOT NULL COMMENT 'App code',
    CLIENT_CODE CHAR(8) NOT NULL COMMENT 'Client code',
    CODE CHAR(22) NOT NULL COMMENT 'Unique code',
    NAME VARCHAR(64) COMMENT 'Rule name',
    DESCRIPTION TEXT COMMENT 'Rule description',
    PRODUCT_ID BIGINT UNSIGNED NULL COMMENT 'Product ID (nullable, one of productId or productTemplateId)',
    PRODUCT_TEMPLATE_ID BIGINT UNSIGNED NULL COMMENT 'Product template ID (nullable)',
    SOURCE CHAR(32) NOT NULL COMMENT 'Source for which rule applies',
    EXPIRY_DAYS INT UNSIGNED NOT NULL COMMENT 'Days of inactivity before ticket expiration',
    TEMP_ACTIVE TINYINT(1) NOT NULL DEFAULT 0 COMMENT 'Temporary active flag',
    IS_ACTIVE TINYINT(1) NOT NULL DEFAULT 1 COMMENT 'Active flag',
    CREATED_BY BIGINT UNSIGNED COMMENT 'User who created this row',
    CREATED_AT TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Creation time',
    UPDATED_BY BIGINT UNSIGNED COMMENT 'User who last updated this row',
    UPDATED_AT TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Last update time',

    PRIMARY KEY (ID),
    UNIQUE KEY UK1_PTER_CODE (CODE),
    UNIQUE KEY UK2_PTER_AC_CC_PTID_SOURCE (APP_CODE, CLIENT_CODE, PRODUCT_TEMPLATE_ID, SOURCE),
    UNIQUE KEY UK3_PTER_AC_CC_PID_SOURCE (APP_CODE, CLIENT_CODE, PRODUCT_ID, SOURCE),
    INDEX IDX1_PTER_AC_CC_PTID (APP_CODE, CLIENT_CODE, PRODUCT_TEMPLATE_ID),
    INDEX IDX2_PTER_AC_CC_PID (APP_CODE, CLIENT_CODE, PRODUCT_ID),
    CONSTRAINT FK1_PTER_PID FOREIGN KEY (PRODUCT_ID) REFERENCES entity_processor_products (ID),
    CONSTRAINT FK1_PTER_PTID FOREIGN KEY (PRODUCT_TEMPLATE_ID) REFERENCES entity_processor_product_templates (ID)
) ENGINE = InnoDB DEFAULT CHARSET = UTF8MB4 COLLATE = UTF8MB4_UNICODE_CI;
